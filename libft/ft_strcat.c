#include "libft.h"

char *ft_strcat(char *s1, const char *s2)
{
	size_t a;
	size_t b;

	a = 0;
	b = 0;
	if (s1 != NULL && s2 != NULL)
	{
		while (s1[a] != '\0')
			a++;
		while (s2[b] != '\0')
		{
			s1[a] = s2[b];
			a++;
			b++;
		}
		s1[a] = '\0';
	}
	return (s1);
}
